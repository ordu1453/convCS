#ifndef INC_TYPES_H_
#define INC_TYPES_H_

#include <stdint.h>

/**
 * @brief Структура для хранения значений датчиков
 *
 * Содержит измеренные значения всех аналоговых датчиков системы.
 * Все значения представлены в 32-битном формате с фиксированной точкой
 * или как целые числа с масштабированием.
 *
 * @note Единицы измерения:
 *       - Токи: миллиамперы (мА) или с масштабированием
 *       - Напряжения: милливольты (мВ) или с масштабированием
 *
 * @note Для преобразования в физические величины используйте
 *       коэффициенты из config.h
 */
typedef struct {
    int32_t currentIn;     /**< Входной ток (значение с датчика тока на входе) */
    int32_t currentOut;    /**< Выходной ток (значение с датчика тока на выходе) */
    int32_t currentChoke;  /**< Ток дросселя (значение с датчика тока дросселя) */
    int32_t voltageIn;     /**< Входное напряжение (значение с датчика напряжения на входе) */
    int32_t voltageOut;    /**< Выходное напряжение (значение с датчика напряжения на выходе) */
} SensorValues_t;

#endif /* INC_TYPES_H_ */
