/**
 * @file precharge.h
 * @brief Заголовочный файл модуля управления предзарядом
 *
 * Этот файл содержит объявления функций для управления схемой предзаряда
 * входных конденсаторов преобразователя.
 *
 * @author ordum
 * @date Oct 16, 2025
 */

#ifndef INC_PRECHARGE_H_
#define INC_PRECHARGE_H_

#ifndef PRECHARGE_H
#define PRECHARGE_H

#include <stdint.h>

/**
 * @brief Инициализация модуля предзаряда
 *
 * Выполняет начальную настройку модуля предзаряда.
 * Должна быть вызвана один раз при старте системы.
 */
void prechargeInit(void);

/**
 * @brief Запуск процедуры предзаряда
 *
 * Выполняет процедуру предзаряда входных конденсаторов
 * через токоограничивающий резистор для предотвращения
 * бросков тока при включении.
 *
 * @note Использует блокирующую задержку на 5 секунд
 * @warning Должна вызываться только при безопасных условиях
 */
void prechargeStart(void);

#endif // PRECHARGE_H

#endif /* INC_PRECHARGE_H_ */
